  static NavigatorMaterial _toMaterial(int i, _SeedRow row, DateTime now) {
    final id = 'nav-${i + 1}';
    final actions = _actionPackFor(row);

    final source = _sourcePack(row);

    return NavigatorMaterial(
      id: id,
      slug: row.slug,
      title: row.title,
      subtitle: 'Практический маршрут без воды',
      excerpt: row.shortDescription.isEmpty
          ? 'Сначала смысл, потом конкретика, затем действие в AURIX.'
          : row.shortDescription,
      bodyBlocks: _bodyBlocksFor(row),
      category: row.category,
      tags: row.tags,
      blockers: row.blockers,
      platforms: row.platforms,
      stages: row.stages,
      goals: row.goals,
      difficulty: row.difficulty ??
          (i % 3 == 0 ? 'базовый' : (i % 3 == 1 ? 'средний' : 'продвинутый')),
      readingTimeMinutes: row.readingTime ?? (5 + (i % 6)),
      formatType: i % 6 == 0
          ? 'статья'
          : (i % 6 == 1
              ? 'чеклист'
              : (i % 6 == 2
                  ? 'видео'
                  : (i % 6 == 3
                      ? 'шаблон'
                      : (i % 6 == 4 ? 'кейс' : 'маршрутный шаг')))),
      actionLinks: actions,
      sourcePack: source,
      lastReviewedAt: now.subtract(Duration(days: i * 3)),
      isFeatured: i < 6,
      isPublished: true,
      priorityScore: row.priorityScore ?? (0.55 + ((30 - i) / 100)),
      relatedContentIds: row.relatedArticles.isNotEmpty
          ? row.relatedArticles
          : [
              'nav-${(i % 30) + 1}',
              'nav-${((i + 7) % 30) + 1}',
            ],
      relatedTools: row.relatedTools,
      createdAt: now.subtract(Duration(days: 40 + i)),
      updatedAt: now.subtract(Duration(days: i ~/ 2)),
    );
  }

  static List<NavigatorActionLink> _actionPackFor(_SeedRow row) {
    final out = <NavigatorActionLink>[];
    final goal = row.goals.map((e) => e.toLowerCase()).join(' ');
    final isYandex = row.category == NavigatorClusters.yandexMusic;
    final isVk = row.category == NavigatorClusters.vkMusic;
    final isLegal = row.category == NavigatorClusters.legalSafety ||
        row.category == NavigatorClusters.contractsRights ||
        row.category == NavigatorClusters.artistBrand;

    if (isYandex || isVk || goal.contains('релиз')) {
      out.add(const NavigatorActionLink(
        actionType: 'open_release_planner',
        label: 'Открыть Release',
        route: '/releases/create',
      ));
    }
    if (isYandex || isVk || goal.contains('продвиж')) {
      out.add(const NavigatorActionLink(
        actionType: 'open_promo',
        label: 'Открыть Promo',
        route: '/promo',
      ));
    }
    if (isLegal || goal.contains('прав') || goal.contains('монет')) {
      out.add(const NavigatorActionLink(
        actionType: 'open_finances',
        label: 'Открыть Финансы',
        route: '/finance',
      ));
    }
    if (!isLegal) {
      out.add(const NavigatorActionLink(
        actionType: 'open_studio_ai',
        label: 'Открыть Studio AI',
        route: '/ai',
      ));
    }
    out.add(const NavigatorActionLink(
      actionType: 'save_to_route',
      label: 'Сохранить в мой маршрут',
      route: '/navigator/saved',
    ));
    return out.take(4).toList();
  }

