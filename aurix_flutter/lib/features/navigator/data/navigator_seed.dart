        ? 'Артистам, которые хотят ясную и рабочую систему роста.'
        : 'Артистам на этапе: ${row.stages.take(2).join(' / ')}.';
    final intro = spec.introOverride ?? _buildLongReadIntro(row, spec);
    final howItWorks = spec.howItWorksOverride ?? _buildHowItWorks(row);
    final misconceptions =
        spec.misconceptionsOverride ?? _buildMisconceptions(row);
    final example = spec.exampleOverride ?? _buildExample(row);
    final takeaway = spec.finalTakeaway.isNotEmpty
        ? spec.finalTakeaway
        : 'Сильный результат в этой теме появляется, когда ты действуешь не разово, а в повторяемом цикле с контролем качества решений.';
    return _longRead(
      row: row,
      audience: audience,
      intro: intro,
      howItWorks: howItWorks,
      misconceptions: misconceptions,
      mistakes: spec.mistakes,
      practicalSteps: spec.actionSteps,
      example: example,
      takeaway: takeaway,
      actions: spec.actionSteps.take(4).toList(),
    );
  }

