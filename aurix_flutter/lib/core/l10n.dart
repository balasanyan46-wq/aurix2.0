/// InheritedWidget to provide locale. Wrap app with L10nScope(locale: ..., child: ...).
class L10nScope extends InheritedWidget {
  final AppLocale locale;

  const L10nScope({super.key, required this.locale, required super.child});

  static AppLocale of(BuildContext context) {
    final w = context.dependOnInheritedWidgetOfExactType<L10nScope>();
    return w?.locale ?? AppLocale.en;
  }

  @override
  bool updateShouldNotify(L10nScope old) => locale != old.locale;
}

